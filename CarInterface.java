
public interface CarInterface {
  /**
   * This method returns the brand of the vehicle as a String
   * 
   */
  public String Brand();

  /**
   * This method returns the model of the car as a String
   * 
   */
  public String Model();

  /**
   * This method returns the year of the car
   * 
   */
  public int Year();

  /**
   * This method returns the price
   * 
   */
  public double Price();

  /**
   * This method returns the mileage
   * 
   */
  public double Mileage();
}
